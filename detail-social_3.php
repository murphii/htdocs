                <p>พม.ร่วมกับภาคีเครือข่ายจัดงานวันคนพิการสากล มอบโล่คนพิการต้นแบบให้ ‘น้องทาม’ หนูน้อยไร้แขนชาวพิษณุโลกท่ีร่วมกิจกรรมปั่นเพื่อแม่ ส่วน ‘มาดามแป้ง-นวลพรรณ ล่ำซำ’ รับรางวัลองค์กรธุรกิจสนับสนุนงานด้านคนพิการดีเด่นต่อเนื่อง...
				<br><br>
				เมื่อวันที่ 3 ธ.ค.2558 ที่ห้องรอยัลจูบิลี่ บอลรูม เมืองทองธานี กรมส่งเสริมและพัฒนาคุณภาพชีวิตคนพิการ (พก.) ร่วมกับภาคีเครือข่าย จัดงานวันคนพิการสากล ประจำปี 2558 ภายใต้แนวคิด “สังคมบูรณาการด้านการเข้าถึงและการเสริมพลังเพื่อคนทั้งมวล”
				<br><br>
				พล.ต.อ.อดุลย์ แสงสิงแก้ว รมว.การพัฒนาสังคมและความมั่นคงของมนุษย์ (พม.) กล่าวในพิธีเปิดงานว่า ในปี 2558 นี้ องค์การสหประชาชาติได้กำหนดประเด็นหลักของวันคนพิการสากล คือ “สังคมบูรณาการด้านการเข้าถึงและการเสริมพลังเพื่อคนทั้งมวล มีจุดมุ่งเน้น 3 ประการ คือ 1. การจัดชุมชนเมืองให้ทุกคนสามารถเข้าถึงและใช้ประโยชน์ได้ 2. การปรับปรุงระบบข้อมูลและสถิติด้านคนพิการ และ 3. การเสริมพลังคนพิการให้เป็นส่วนหนึ่งของสังคมและการพัฒนา
				<br><br>
				ทั้งนี้ พม.ให้ความสำคัญต่อการพัฒนาคุณภาพชีวิตคนพิการอย่างต่อเนื่อง โดยในปี 2559 ยังคงขับเคลื่อนการจัดสิ่งอำนวยความสะดวกสำหรับคนพิการ ผู้สูงอายุ และทุกคนในสังคม การส่งเสริมอาชีพคนพิการในชุมชน การพัฒนาศักยภาพนักร้องนักดนตรีตาบอด ให้สามารถประกอบอาชีพและมีรายได้ และการพัฒนานวัตกรรมเทคโนโลยีสิ่งอำนวยความสะดวกสำหรับคนพิการ
				<br><br>	
				จากนั้น พล.ต.อ.อดุลย์ ได้มอบโล่ประกาศเกียรติคุณแก่คนพิการต้นแบบ รวมทั้งหน่วยงานองค์กรดีเด่นที่สนับสนุนงานด้านคนพิการจำนวน 166 รางวัล ซึ่งหนึ่งในผู้รับรางวัลคนพิการต้นแบบ คือ ด.ช.วรรณธนะ คำอินทร์ หรือน้องทาม นักเรียน ป.5 ร.ร.วัดหนองนาดงกวาง อ.บางระกำ จ.พิษณุโลก ซึ่งพิการแขนทั้งสองข้าง ที่ได้สร้างความฮือฮาจากการร่วมปั่นจักรยานในกิจกรรม “ปั่นเพื่อแม่ Bike for Mom” เมื่อวันที่ 16 ส.ค.ที่ผ่านมา และกำลังเข้าร่วม “ปั่นเพื่อพ่อ Bike for Dad” ในวันที่ 11 ธ.ค.นี้
				<br><br>
				น้องทาม ให้สัมภาษณ์ว่า รู้สึกดีใจและภูมิใจกับรางวัลที่ได้รับและอยากเชิญชวนให้ทุกคนมาร่วมกิจกรรมสำคัญในการปั่นจักรยานเพื่อพ่อหลวงของเรา
				<br><br>
				นอกจากนี้ ยังมี นางนวลพรรณ ล่ำซำ กรรมการผู้จัดการและประธานเจ้าหน้าที่บริหารบริษัท เมืองไทยประกันภัย จำกัด (มหาชน) เข้ารับรางวัลองค์กรธุรกิจสนับสนุนงานด้านคนพิการดีเด่นต่อเนื่อง โดยนางนวลพรรณ กล่าวว่า ทุกภาคส่วนมีส่วนส่งเสริมสนับสนุนให้คนพิการซึ่งมีจำนวนไม่น้อยมีศักยภาพ มีความรู้ความสามารถได้มีโอกาสแสดงออกและทำประโยชน์ต่อส่วนรวม เป็นการเปิดพื้นที่ให้คนพิการได้เปลี่ยนจากภาระเป็นพลัง ขณะเดียวกัน ก็อยากให้ทุกหน่วยงานได้เปิดรับคนพิการเข้าทำงาน เพื่อสร้างอาชีพสร้างรายได้ให้กับผู้พิการ.</p>

