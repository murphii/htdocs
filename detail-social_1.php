                <p>กรุงเทพฯ--29 ธ.ค.--กระทรวงการพัฒนาสังคมและความมั่นคงของมนุษย์

วันนี้ (๒๘ ธ.ค.๕๘) เวลา ๑๐.๓๐ น. นายไมตรี อินทุสุต ปลัดกระทรวงการพัฒนาสังคมและความมั่นคง ของมนุษย์ (ปพม.) เป็นประธานในพิธีเปิดงาน "รณรงค์ปีใหม่หยุดความพิการบนท้องถนน" โดยมีผู้เข้าร่วมงานจำนวนทั้งสิ้น ๒๐๐ คน ประกอบด้วยคนพิการ องค์กรด้านคนพิการ สมาชิกมูลนิธิเมาไม่ขับ และผู้แทนของหน่วยงานที่เกี่ยวข้องณ อาคารผู้โดยสารขาออกสถานีขนส่งหมอชิตใหม่ กรุงเทพฯ

	นายไมตรี กล่าวว่า กระทรวงการพัฒนาสังคมและความมั่นคงของมนุษย์ (พม.) โดยกรมส่งเสริมและพัฒนาคุณภาพชีวิตคนพิการ (พก.) มีภารกิจหน้าที่ในการประสานและบูรณาการความร่วมมือ ทั้งในเชิงนโยบายและยุทธศาสตร์สำหรับการส่งเสริมและพัฒนาคุณภาพชีวิตคนพิการ ตลอดจนการเฝ้าระวังและป้องกันความพิการ ได้ตระหนักถึงความสำคัญของการเฝ้าระวังและป้องกันความพิการ รวมทั้งการลดอัตราการเกิดอุบัติเหตุบนท้องถนน ซึ่งเป็นสาเหตุสำคัญของความพิการและการสูญเสียชีวิตของประชาชน ทั้งนี้มีสถิติของคนพิการในประเทศไทย โดยจากการทำบัตรประจำตัวคนพิการ พบว่ามีจำนวนทั้งสิ้น ๑,๖๔๒,๔๔๘ คน ซึ่งมีสาเหตุที่น่าสนใจของความพิการพบว่า เกิดจากอุบัติเหตุจำนวน ๑๔.๑๔ เปอร์เซ็นต์และมีจำนวนที่เพิ่มมากขึ้นทุกปี กระทรวงการพัฒนาสังคมฯ โดย พก. จึงได้ประสานและบูรณาการร่วมกับบริษัท ขนส่ง จำกัด และมูลนิธิเมาไม่ขับ จัดงาน "รณรงค์ปีใหม่หยุดความพิการบนท้องถนน" เพื่อเสริมสร้างความรู้ ความเข้าใจและความตระหนักในการเฝ้าระวังและป้องกันความพิการ ตลอดจนรณรงค์ป้องกันความพิการที่เกิดจากอุบัติเหตุบนท้องถนนในเทศกาลปีใหม่ พ.ศ. ๒๕๕๙ ที่จะมาถึงในครั้งนี้

	"สำหรับกิจกรรมภายในงานประกอบด้วย การรณรงค์ให้กับประชาชนที่กำลังเดินทางกลับภูมิลำเนาในเทศกาลปีใหม่เพื่อให้ตระหนักถึงการเกิดอุบัติเหตุบนท้องถนนซึ่งเป็นสาเหตุของความพิการและการเสียชีวิต กิจกรรมให้ความรู้เกี่ยวกับอุบัติเหตุบนท้องถนน การแสดงดนตรีของนักร้องพิการ เพื่อสร้างความบันเทิงให้กับพี่น้องประชาชนที่กำลังเดินทางกลับภูมิลำเนาต่างจังหวัด เป็นต้น อย่างไรก็ตาม การจัดงานครั้งนี้ จะสร้างความตระหนักแก่ประชาชนในการเฝ้าระวังและป้องกันการเกิดความพิการบนท้องถนนในช่วงเทศกาลปีใหม่" นายไมตรี กล่าวในตอนท้าย
