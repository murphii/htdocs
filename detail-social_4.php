                <p>หากคุณมีความใฝ่ฝันที่จะได้ทำงานกับองค์กรชั้นนำ คุณไม่ควรรอโอกาส แต่ควรรีบไขว่คว้าหาโอกาส เพื่อทำให้ความฝันเป็นความจริง แม้ว่าองค์กรที่คุณฝันจะยังไม่เปิดรับบุคลากรในตำแหน่งงาน ที่คุณสนใจ คุณก็สามารถนำเสนอตัวคุณ ความสามารถของคุณที่น่าสนใจให้นายจ้าง พิจารณาได้จดหมายสมัครงานเป็นส่วนสำคัญมากอย่างหนึ่งที่จะทำให้คุณได้งานที่ต้องการคุณจำเป็นต้องโฆษณาตัวคุณให้นายจ้างสนใจ และเรียกสัมภาษณ์งานให้ได้ นี่คือขั้นตอนแรกของการ สมัครงานที่คุณไม่ควรละเลย
					<br><br>
						ก่อนอื่นควรศึกษาหาข้อมูลเกี่ยวกับบริษัทที่คุณต้องการร่วมงานด้วยให้ได้มากที่สุด เพื่อที่คุณจะสามารถ เขียนจดหมายสมัครงานเพื่อจูงในให้นายจ้างสนใจคุณและเก็บคุณไว้พิจารณาทันทีที่ตำแหน่งงานดังกล่าวเปิดรับสมัคร นอกจากนี้ หากคุณรู้ว่าบุคคลที่รับผิดชอบในการว่าจ้าง รับสมัครงานคือใครจะทำให้คุณได้เปรียบกว่า โดยคุณสามารถส่งอีเมลจดหมายสมัคร งานถึงบุคคลที่มีหน้าที่รับผิดชอบและมีอำนาจตัดสินใจโดยตรง แทนที่จะส่งถึงเจ้าหน้าที่ฝ่ายทรัพยากรบุคคลเหมือนจดหมาย สมัครงานทั่ว ๆ ไปซึ่งไม่เฉพาะเจาะจง
					<br><br>
						ทำลิสต์คุณสมบัติของคุณทุกอย่างที่คิดว่าสอดคล้องกับตำแหน่งงานที่คุณต้องการสมัคร คุณมีทักษะอะไรบ้างที่ตรงกับความ ต้องการของนายจ้าง ประสบการณ์การทำงานทุกอย่างที่คุณเคยทำ รวมถึงงานอาสาสมัครต่าง ๆ รวบรวมมาเตรียมไว้ให้หมด จนกว่า คุณจะนึกไม่ออกอีกแล้ว
					<br><br>
						ตัดสิ่งที่นอกประเด็น หรือไม่เกี่ยวข้อง ไม่จำเป็นออกไป แล้วเรียงลำดับความสำคัญจาก สิ่งที่สำคัญน้อยที่สุดไปจนถึงสี่งที่สำคัญ มากที่สุด การทำเช่นนี้จะช่วยให้คุณร่างจดหมายที่มีบทสรุปที่น่าประทับใจ
					<br><br>
						มองหาสิ่งที่เป็นช่องว่างหรือจุดอ่อนที่จะทำให้เกิดข้อโต้แย้งเกี่ยวกับตัวคุณ หากพบให้แก้ไขหรือกำจัดจุดอ่อนนั้นเสีย เพื่อป้องกัน ประเด็นปัญหาที่อาจเกิดขึ้นได้จากประวัติที่ผ่านมาของคุณ
					<br><br>
						ภาษาเขียนมีข้อดีที่ต่างจากภาษาพูดคือ คุณสามารถแก้ไขสิ่งที่คุณเขียนได้ ตรงกันข้ามกับสิ่งที่คุณพูดซึ่งไม่สามารถย้อนกลับ ไปแก้ไขได้ หากคุณพบว่าตัวเองวนอยู่กับความคิดเดิม ๆ แล้วล่ะก็ คุณควรวางมือจากการเขียนจดหมายสมัครงานสักครู่ ลองหยิบจับงานอย่างยื่นขึ้นมาทำแทน แล้วค่อยกลับไปลงมือเขียนอีกครั้ง คุณจะพบว่าไอเดียของคุณเริ่มโลดแล่นได้อีกครั้ง ซึ่งทำให้คุณสามารถถ่ายทอด นำเสนอตัวคุณได้ดียิ่งขึ้น
					<br><br>
						เมื่อคุณร่างจดหมายสมัครงานเรียบร้อยแล้ว ควรให้เวลากับการตรวจทานเพื่อให้แน่ใจว่าไม่มีข้อผิดพลาด หรือมีอะไรหาย หกตกหล่นไม่ครบถ้วน และอย่าลืมใส่ใจในเรื่องตัวสะกดด้วย เพราะรายละเอียดเล็ก ๆ น้อย ๆ นี่แหละที่นายจ้างมักใช้ประกอบการ พิจารณาคัดเลือกผู้สมัครงาน วิธีหนึ่งที่จะช่วยให้จดหมายสมัครงานของคุณมีประสิทธิภาพมากยิ่งขึ้นคือ คุณควรปล่อยให้เวลา ผ่านไปสัก 1-2 วันแล้วกลับมาอ่านจดหมายอีกรอบหนึ่ง คุณอาจพบจุดที่ต้องการแก้ไขเพิ่มเติมก็ได้
					<br><br>
						วิธีเขียนจดหมายสมัครงานหากคุณยังไม่มั่นใจว่าจดหมายสมัครงานที่คุณร่างมาดีเพียงพอหรือยัง แนะนำให้พริ้นต์ออกมา แล้วอ่านออกเสียงให้เพื่อน หรือสมาชิกในครอบครัวลองฟังดู รวบรวมความคิดเห็นจากผู้ฟังว่าพวกเขารู้สึกอย่างไร เพื่อนำกลับมาแก้ไขอีกครั้งหนึ่ง อย่าลืมวางอีโก้ของคุณลงก่อน แล้วเปิดใจรับฟังความคิดเห็นจากทุกคนไม่ว่ามันจะออกมาอย่างไรก็ตาม มุมมองของคนอื่น ๆ อาจทำให้คุณเห็นข้อบกพร่อง หรือสิ่งที่คุณหลงลืมไปในจุดที่คุณมองไม่เห็น
					<br><br>
						เมื่อปรับแก้จดหมายจนเป็นที่พอใจแล้ว ก็ถึงเวลาส่งอีเมลสมัครงานไปยังนายจ้างในอนาคตของคุณได้เลย โดยแนบไปพร้อมกับ เรซูเม่ที่คุณใช้เวลาเรียบเรียงมาเป็นอย่างดีเช่นกัน…อ๊ะ ๆ อย่าลืมว่าจดหมายสมัครงานฉบับหนึ่งร่างขึ้นเพื่อหนึ่งบริษัทเท่านั้น
					<br><br>
						หากคุณต้องการความแตกต่าง และน่าสนใจ อย่าใช้จดหมายฉบับเดียวกันในการสมัครงานทุกบริษัทนะคะ
